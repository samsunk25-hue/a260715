/* -------------------------------------------------------------------
   ③ 정규화
      "시*발", "시.발" 처럼 사이에 기호를 끼워 필터를 피하려는 걸
      잡기 위해, 검사할 때만 기호를 빼고 봅니다.

      map은 정규화된 글자가 원래 몇 번째 글자였는지 기억합니다.
      이게 있어야 원문에서 정확한 위치만 골라 가릴 수 있습니다.

   ★ 공백(스페이스)은 절대 지우면 안 됩니다 ★
      공백까지 지우면 아래처럼 멀쩡한 문장이 다 걸립니다.
        "역시 발표를 잘했다"  -> "역시발표를..."  -> "시발" 발견 (!!)
        "도시 발전에 대해"    -> "도시발전에..."  -> "시발" 발견 (!!)
        "혹시 발견한 사람"    -> "혹시발견한..."  -> "시발" 발견 (!!)
      한국어는 '역시/도시/혹시/다시' 뒤에 '발'로 시작하는 말이 오는
      경우가 너무 흔합니다. 실제로 테스트했더니 평범한 문장 11개 중
      7개가 걸렸습니다.

      대신 "시 발"처럼 공백을 끼운 우회는 잡지 못합니다.
      "역시 발표"와 "시 발"은 글자 순서가 똑같아서, 둘을 구분하려면
      문맥을 이해해야 하는데 그건 이 필터의 능력 밖입니다.
      멀쩡한 문장을 망가뜨리는 쪽이 더 나쁘다고 판단했습니다.
   ------------------------------------------------------------------- */
const SKIP = /[.,\-_*~`!@#$%^&()+=[\]{}|\\:;"'<>?/·ㆍ…]/;   // \s 없음 — 위 설명 참고

function normalize(text) {
  let norm = "";
  const map = [];
  for (let i = 0; i < text.length; i++) {
    const ch = text[i];
    if (SKIP.test(ch)) continue;
    norm += ch.toLowerCase();
    map.push(i);
  }
  return { norm, map };
}

/** 문자열 안에서 word가 나오는 모든 [시작, 끝) 구간 */
function ranges(haystack, word) {
  const out = [];
  let from = 0;
  for (;;) {
    const i = haystack.indexOf(word, from);
    if (i === -1) break;
    out.push([i, i + word.length]);
    from = i + 1;
  }
  return out;
}

/* -------------------------------------------------------------------
   ④ 본체

      반환값: { text: 가려진 문장, found: ["시발", ...] }
      found가 비어 있으면 아무것도 안 걸린 겁니다.
   ------------------------------------------------------------------- */
export function clean(input) {
  const text = String(input ?? "");
  if (!text) return { text, found: [] };

  const { norm, map } = normalize(text);

  // 화이트리스트 단어가 차지한 구간을 먼저 표시해 둡니다
  const safeZone = new Set();
  for (const safe of SAFE) {
    for (const [s, e] of ranges(norm, safe.toLowerCase())) {
      for (let i = s; i < e; i++) safeZone.add(i);
    }
  }

  const hide = new Set();   // 가릴 원문 위치
  const found = [];

  for (const bad of BAD) {
    for (const [s, e] of ranges(norm, bad.toLowerCase())) {
      // 화이트리스트 단어 안에서 나온 거면 건너뜀 (시발점의 "시발")
      let inSafe = false;
      for (let i = s; i < e; i++) if (safeZone.has(i)) { inSafe = true; break; }
      if (inSafe) continue;

      if (!found.includes(bad)) found.push(bad);

      // 정규화 위치 → 원문 위치로 되돌려서 표시
      for (let i = s; i < e; i++) hide.add(map[i]);
    }
  }

  if (!hide.size) return { text, found: [] };

  // 글자 수를 그대로 유지하며 가립니다 (커서가 튀지 않도록)
  const out = [...text];
  for (const i of hide) out[i] = "●";
  return { text: out.join(""), found };
}

/** 걸리는 게 있는지만 빠르게 확인 */
export function hasBadWords(input) {
  return clean(input).found.length > 0;
}
